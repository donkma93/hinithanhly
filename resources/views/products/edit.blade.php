                x-data="{
                    supplierTypes: @js($supplierTypeMap),
                    manualTypes: @js(\App\Models\Supplier::MANUAL_CONSIGNMENT_TYPES),
                    autoTypes: @js(\App\Models\Supplier::AUTO_CONSIGNMENT_TYPES),
                    selectedSupplierId: @js((string) old('supplier_id', $product->supplier_id)),
                    init() {
                        window.addEventListener('searchable-select-change', (event) => {
                            if (event.detail?.name !== 'supplier_id') {
                                return;
                            }

                            this.selectedSupplierId = String(event.detail?.value ?? '');
                        });
                    },
                    get selectedSupplierType() {
                        return this.supplierTypes[this.selectedSupplierId] ?? '';
                    },
                    get requiresManualConsignment() {
                        return this.manualTypes.includes(this.selectedSupplierType);
                    },
                    get usesAutoGeneratedConsignment() {
                        return this.autoTypes.includes(this.selectedSupplierType);
                    },
